/**
 * Search emojis by shortcode
 */
export function searchEmojis(query: string, limit: number = 8): Array<{ code: string; emoji: string }> {
  if (!query) return [];

  const queryLower = query.toLowerCase();
  const results: Array<{ code: string; emoji: string; score: number }> = [];

  for (const [code, emoji] of Object.entries(emojis)) {
    if (code.startsWith(queryLower)) {
      results.push({ code, emoji, score: 100 - code.length });
    } else if (code.includes(queryLower)) {
      results.push({ code, emoji, score: 50 - code.indexOf(queryLower) });
    }
  }

  return results
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(({ code, emoji }) => ({ code, emoji }));
}
